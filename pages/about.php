<div class="article content-text"><h1>Der Wettbewerb</h1>
<p>Formel 1 in der Schule ist ein multidisziplinärer, internationaler
  Technologie-Wett­bewerb, bei dem Schülerinnen und Schüler im Alter von 11 bis
  19 Jahren einen Miniatur-Formel 1 Rennwagen am Computer entwickeln, fertigen
  und anschließend ins Rennen schicken.</p>

<p>Das Ziel ist es, die von der "großen" Formel 1 ausgehende Faszination und
  weltweite Präsenz zu nutzen, um für die Jugend ein aufregendes, spannendes
  Lernerlebnis zu schaf­fen, damit das Verständnis und den Einblick in die
  Be­reiche Produktentwicklung, Technologie und Wissenschaft zu verbessern und
  berufliche Laufbahnen in der Technik aufzuzeigen.</p>

<p>In Regionalwettkämpfen und Landesmeisterschaften treten die mit Gaspatronen
  angetriebenen Boliden auf einer 20 m langen Rennstrecke gegeneinander an. Die
  Siegerteams vertreten ihr Bundesland auf der Deutschen Meisterschaft.</p>

<p>Entscheidend ist die Teamleistung aus Konstruktion, Ferti­gung, Reaktionszeit,
   Fahrzeuggeschwindigkeit, Business­plan und Präsentation.</p>

