<div class="article">
  <h1>Die Autos sind gefräst</h1>
  <div class="content-img"><img class="content-img-left-quad" src="media/fraesen.jpg"></div>
  <br>
  <p>
    Nachdem die fertig konstruierten Chassis den Weg zum Fräser erfolgreich überstanden haben und die
  Front- und Heckspoiler in dem 3D-Drucker der FH Münster gedruckt wurden, konnte unser Team das
  Chassis und die Spoiler zusammensetzen, damit das zusammengesetzte Auto am Montag mit dem
  geplantem Autodesign zu der Firma Lennartz, unserem Lackierer, gebracht werden konnte. Nach
  mühevoller Feil- und Klebarbeit konnte das Auto abgeklebt werden, um nach dem Lackiervorgang ein
  Anbringen von Achsenkonstruktionen mit Rädern zu ermöglichen. An dieser Stelle nochmal vielen Dank
  an die Firma Brüssow, welche unsere 4 Balsaholzblöcke mit der CNC-Fräse in die passende Form gefräst
  hat. Außerdem danken wir der FH Münster und der Firma Lennartz für die bereits oben geschilderten
  Erledigungen und hoffen, dass unsere Autos den restlichen Fertigungsprozess heile überstehen und uns in
  etwa 3 Wochen beim Wettbewerb in Nordrhein-Westfalen bereitstehen.
