<h1>Wir sind das Team:</h1>
<p>Wir sind ein sechs Köpfiges Team aus, engagierten und motivierten, Schüler vom Kardinal-
von-Galen Gymnasium Münster. Da schon ein Großteil unseres Teams vor zwei Jahren an
dem Junior Projekt teilgenommen hat profitieren wir von ihren Erfahrungen, Fähigkeiten
und Know-how.</p><br>
<h2>Warum haben wir uns den Namen Prometheus ausgesucht?</h2>
<p>Prometheus war ein griechischer Titan der den Tieren Wehrhaftigkeit, Stärke, und
Schnelligkeit gebracht hat. Genau das haben wir uns als Motto genommen um uns als Team,
beim Formel 1 Projekt, gegen die anderen Gruppen durchzusetzen. Außerdem soll auch
unser Auto diese Eigenschaften verkörpern.</p>
